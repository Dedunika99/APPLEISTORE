    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            setVisible(false);
            String username = userName.getText().trim();
            if (username.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Username cannot be empty.");

                createAccount ca = new createAccount();
                ca.setVisible(true);
                return;
            }

            // Validate password
            String password = new String(jPasswordField1.getPassword()).trim();
            if (!isValidPassword(password)) {
                JOptionPane.showMessageDialog(this, "Password must be at least 8 characters long and contain at least one uppercase letter, one lowercase letter, one digit, and one special character.");

                createAccount ca = new createAccount();
                ca.setVisible(true);
                return;
            }

            // Validate confirm password
            String confirmPassword = new String(jPasswordField2.getPassword()).trim();
            if (!confirmPassword.equals(password)) {
                JOptionPane.showMessageDialog(this, "Passwords do not match.");

                createAccount ca = new createAccount();
                ca.setVisible(true);
                return;
            }

            // Validate user type
            String userType = (String) jComboBox1.getSelectedItem();
            if (userType.equals("Select User Type")) {
                JOptionPane.showMessageDialog(this, "Please select a user type.");

                createAccount ca = new createAccount();
                ca.setVisible(true);
                return;
            }

            // Check if username already exists
            String query = "SELECT * FROM user_details WHERE username = ?";
            pst = db.con.prepareStatement(query);
            pst.setString(1, username);
            rs = pst.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Username already exists.");

                createAccount ca = new createAccount();
                ca.setVisible(true);
                return;
            }

            // Insert new user
            query = "INSERT INTO user_details(username, password, userType) VALUES(?, ?, ?)";
            pst = db.con.prepareStatement(query);
            pst.setString(1, username);
            pst.setString(2, password);
            pst.setString(3, userType);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Account created successfully!");

            // Clear form fields
            userName.setText("");
            jPasswordField1.setText("");
            jPasswordField2.setText("");
            jComboBox1.setSelectedIndex(0);

            createAccount ca = new createAccount();
            ca.setVisible(true);
            

        } catch (SQLException ex) {
            Logger.getLogger(createAccount.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "An error occurred while creating the account. Please try again later.");

        }

    }

